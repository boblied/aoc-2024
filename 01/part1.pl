use v5.40;

my @list;
while (<>)
{
    my @id = split;
    push @{$list[0]}, $id[0];
    push @{$list[1]}, $id[1];
}

$list[0] = [ sort { $a <=> $b } $list[0]->@* ];
$list[1] = [ sort { $a <=> $b } $list[1]->@* ];

my $sum = 0;

for ( 0 .. $list[0]->$#* )
{
    $sum += abs($list[0][$_] - $list[1][$_]);
}
say $sum;
