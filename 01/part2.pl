use v5.40;

my %id;
my %freq;
while (<>)
{
    my @id = split;
    $id  { $id[0] }++;
    $freq{ $id[1] }++;
}

my $sum = 0;

for my $i ( keys %id )
{
    # say "ID: $i";
    for my $n ( 1 .. $id{$i} )
    {
        $sum += $i * ( $freq{$i} // 0 );
        # say "ID: $i->$n: $i * ", ($freq{$i} // 0);
    }
}
say $sum;
