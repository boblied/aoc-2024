use v5.40;

my $sum = 0;
while (<>)
{
    chomp;
    $sum += mult($_);
}
say $sum;

sub mult($s)
{
    my $sum = 0;
    while ( $s =~ m/(mul\((\d+),(\d+)\))/g )
    {
        say "$1 $2 $3";
        $sum += $2 * $3;
    }
    return $sum;
}
